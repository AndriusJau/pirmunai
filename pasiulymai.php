Kelionės į Tailandą praktiškai neapsieina be vizito į Bankoką. Tailando sostinė ir didžiausias miestas, kuriame gyvena maždaug 10 mljn., gyventojų. Dydžiu miestas stipriai išsiskiria iš kitų Tailando miestų. Taip pat tai pagrindinis šalies jūrų ir upių uostas, yra tarptautinis oro uostas. Taip pat Bankokas garsus kultūrinis bei istorinis centras, čia daugybė lankytinų objektų, tokių kaip karališkųjų rūmų kompleksas Čakri (Mahaprasatas XVIII-XX a.), daug šventyklų – Po (arba Četuponas, XIX a.), Radžabopitas (XIX a., vid.), Benčamabopitas ir kt. Taip pat nepraleiskite aplankyti ir miesto vertybių – Bankoko meno ir kultūros centro (BACC), įvairių tradicinių turgų bei mugių (Or Tor Kor vaisių turgus, Chatuchak savaitgalio turgus), šventyklų ir kitų objektų. Kita vertus, Tailandas stipriai auganti ir labai komercinė vieta, čia išvysite daugybę keistenybių, bei šiuolaikinį Tailandą, kuris pakankamai pasikeitė nuo XX amžiaus vidurio.

Paplūdimius ir salas

Tailando salos ir paplūdimiai kiekvienais metais pritraukia milijonus lankytojų. Čia įsikūrę daugybė kurortų bei poilsiaviečių, tokių kaip Hua Hin, Pattaya, Phuket, Ko Samui, kurios pasiūlys reikiamą turistinę infrastruktūrą bei poilsį pajūryje. Ieškant labiau alternatyvesnės poilsinės kelionės į Tailandą verta atkreipti dėmesį į Krabi provinciją, kuri taip pat patraukli paplūdimių mylėtojams, aplankykite Ao Nang, Rai Lehbei auksinius Ko Lantapaplūdimius. Tailandui priklauso šimtai salų esančių Siamo įlankoje ir Andamanų jūroje. Visos salos yra netoli kranto, atviroje jūroje. Anksčiau daugelis Tailandui priklausančių salų buvo negyvenamos, bet paskutiniu metu daugelis jų plėtojamos turizmo tikslais. Salose atrasite mažyti rojų, per dienas galėsite gulėti hamake iš kurio žvelgsite į jūros tolius ir grožėsitės tropine gamta. Gali būti, jog nebenorėsite grįšti namo.

Gamtines vertybės

Nors ir ne tokie įspūdingi kaip Indonezijos ar Malaizijos, Tailando tropiniai miškai taip pat verti dėmesio. Čia įsteigtas ne vienas nacionalinis parkas – Khao Yaibei Khao Soknacionaliniai parkai. Čia galėsite pamatyti makakas, gibonus, įvairių rūšių egzotinius paukščius, taip pat elnius, pražygiuoti džiunglių takais, netgi praleisti jose naktį. Kriokliai stūkso visoje šalyje, kaip ir daugybė kitų gamtos stebuklų, tropinių augalų bei gyvūnų, uolų ir skardžių, kalnų bei kitų gamtinių grožybių.

Ao Phang-Nga nacionalinis jūros parkas

Andamanų jūros pakrantė nusėta žaliuojančių skardžių, keistų uolų darinių bei žvejų kaimų. Krantus skalauja skaidri žydra jūra, o nuo žemyno krantų matosi išsibarsčiusios salos, žadančios rojų. Esant čia galite išsinuomoti kanoją, baidarę ar privatų laivą ir apžiūrėti jūroje esančias salas, pasinerti į nepakartojamą povandeninį pasaulį. Taip pat organizuojamos ekskursijos ir turai, kurias nusipirkti galite daugelyje turistinių centrų.

